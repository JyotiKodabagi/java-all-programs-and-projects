package advancePattern;

public class alphabeticalProblem {
	public static void main(String[] args) {
		for(char j =65;j<=68;j++) {
			
			for (int i =0; i<=j-65;i++) {
				System.out.print(j);
				}
			System.out.println();
		

		}
	for(char j =67;j>=65;j--) {
			
			for (int i =0; i<=j-65;i++) {
				System.out.print(j);
				}
			System.out.println();
		


	}}

}
